export function mapAIData(aiData) {

    return {

        first_name: {

            value:
                aiData.first_name

        },

        last_name: {

            value:
                aiData.last_name

        },

        email: {

            value:
                aiData.email

        },

        country_of_birth: {

            value:
                aiData.country_of_birth

        }

    };

}